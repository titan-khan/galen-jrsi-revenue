export const getFollowingMetrics = () => logistiqMetricsData.filter((m) => m.isFollowing);

export const getAllMetrics = () => logistiqMetricsData;

export const getMetricById = (id: string) => logistiqMetricsData.find((m) => m.id === id);

export const getMetricsByDomain = (domain: string) =>
  logistiqMetricsData.filter((m) => m.domain === domain);

export const getDomainCategories = () =>
  Array.from(new Set(logistiqMetricsData.map((m) => m.domain).filter(Boolean))) as string[];

export const searchMetrics = (query: string) => {
  const q = query.toLowerCase();
  return logistiqMetricsData.filter(
    (m) =>
      m.name.toLowerCase().includes(q) ||
      m.description.toLowerCase().includes(q) ||
      (m.domain && m.domain.toLowerCase().includes(q))
  );
};
